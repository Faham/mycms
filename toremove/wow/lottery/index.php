	<a id="test"></a>
	<h2>What kind of test is the reaction time test?</h2>
	<p>The <a>Implicit Association Test</a> is a reaction time task that helps us to evaluate the automatic associations players have between themselves and their character. There are frequently asked questions and answers about the <a href="https://implicit.harvard.edu/implicit/demo/background/faqs.html" target="_blank">IAT</a>.</p>
	<p>We are interested in the relationship between characters and players in terms of motivational concepts. Psychology has shown that enjoyment increases when our environment fulfills certain requirements. To learn about your implicit relation to your character, do our test: <a href="http://fluidsurveys.usask.ca/s/wow-survey/"-->get started!</a></p>
	<p>After you finish the test, your results are reported back to you, so that you can learn about your personal relationship to your character; your results will not be shared in any other way.</p>

	<a id="results"></a>
	<h2>Tell me about my results!</h2>

	<p>One goal of this study is to determine, if it is possible to measure the relationship between a player and their character using the IAT. The results reported back to you describe where you fit on a spectrum. We are studying three ways of characterizing relationship between players and their characters: autonomy, competence, and relatedness.</p>

	<h3>Autonomy</h3>
	<p>The relationship between a person and his/her character can be described by the relation between autonomy experienced in everyday life and in the game. Think about autonomy as the restrictions or freedom you experience. For instance, a job might not allow an employee to make any decisions by themself (i.e., a lot of restriction in everyday life) leading to a low "experienced autonomy". On the other hand, the game might give a player a lot of freedom, meaning that the player experiences high autonomy in the game. The IAT results may reflect this scenario by showing a stronger preference for "Character" compared to "Myself". Your results describe how you experience autonomy in everyday life compared to how you experience autonomy in the game.</p>

	<h3>Competence</h3>
	<p>The relationship between a person and his/her character can be described by the relation between competence experienced in everyday life and in the game. For instance, in everyday life, competence is experienced frequently. In general, a person feels capable in carring out everyday tasks. In the game that might not be the same. PvP or PvE challenges are lost frequently. The results of the IAT may reflect this scenario by showing a stronger preference for "Myself" compared to "Character". Your results describe the relation how you experience competence in everyday life compared to how you experience competence in the game.</p>

	<h3>Relatedness</h3>
	<p>The relationship between a person and his/her character can be described by the relation between relatedness experienced in everyday life and in the game. For instance, a person might have close friends who are present in everyday life as well as in the game. The results from the IAT may reflect this scenario by showing neither a preference for "Myself" nor for "Character". Your results describe the relation between how you experience relationship in everyday life compared to how you experience relationship in the game.</p>

	<h3>Interpretation</h3>
	<p>The results of your IAT test get displayed to you are reflective of your experience of either autonomy, comptence, or relatedness in your everyday world compared to your experience in the game world. By measuring reaction time differences to concepts related to these factors, the IAT helps us to quantify your experience. No matter which of the three tests you got assigned to, if a reaction time difference between different pairings of a categorie was so great as to be obvious to you, it would likely be labeled a 'strong' effect. The 'moderate' label also indicates a difference large enough so that you would probably notice it. A 'slight' effect is one that is noticeable in statistical analysis, but you may not have been aware of it.</p>

